'''Store the principal amount, rate of interest, and time in different variables and then calculate 
the Simple Interest for 3 years. Formula: Simple Interest = P x R x T / 100'''
P = 1000  # Principal amount
R = 5     # Rate of interest
T = 3     # Time in years
simple_interest = (P * R * T) / 100
print("Simple Interest for 3 years is:", simple_interest)